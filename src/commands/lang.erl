-spec exec(DispatcherPid, Uid, Lang) -> no_return() when
    Uid :: atom(),
    Lang :: atom(),
    DispatcherPid :: pid().
exec(DispatcherPid, Uid, Lang) ->
    case Lang of
        <<"all">> ->
            player_fsm:show_langs(DispatcherPid, Uid);
        _ ->
            player_fsm:switch_lang(DispatcherPid, Uid, Lang)
    end.

