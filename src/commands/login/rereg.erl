%%--------------------------------------------------------------------
%% @doc
%% re-register user
%%
%% @end
%%--------------------------------------------------------------------
-spec exec(DispatcherPid, UidProfile) -> no_return() when
    UidProfile :: command_dispatcher:uid_profile(),
    DispatcherPid :: pid().
exec(DispatcherPid, UidProfile) ->
    #{uid := Uid} = UidProfile,
    login_server:register_uid(DispatcherPid, Uid).

