%%--------------------------------------------------------------------
%% @doc
%% Test whether a module exists
%%
%% @end
%%--------------------------------------------------------------------
-spec is_module_exists(Module) -> boolean() when
    Module :: atom().
is_module_exists(Module) ->
    case is_atom(Module) of
        true ->
            try Module:module_info() of
                _InfoList ->
                    true
            catch
                _:_ ->
                    false
            end;

        false ->
            false
    end.

